package domain;

public class MemberVO {
	private String id;
	private String pw;
	private int age;
	private String email;
	private String regdate;
	private String moddate;
	
	public MemberVO() {}
	
	//로그인용 생성자
	public MemberVO(String id, String pw) {
		super();
		this.id = id;
		this.pw = pw;
	}
	
	//회원 등록, 수정용 생성자(join, modify)
	public MemberVO(String id, String pw, int age, String email) {
		super();
		this.id = id;
		this.pw = pw;
		this.age = age;
		this.email = email;
	}
	
	//회원리스트 출력용 생성자
	public MemberVO(String id, String pw, int age, String email, String regdate, String moddate) {
		super();
		this.id = id;
		this.pw = pw;
		this.age = age;
		this.email = email;
		this.regdate = regdate;
		this.moddate = moddate;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}

	public String getModdate() {
		return moddate;
	}

	public void setModdate(String moddate) {
		this.moddate = moddate;
	}

	@Override
	public String toString() {
		return "id=" + id + ", pw=" + pw + ", age=" + age + ", email=" + email + ", regdate=" + regdate
				+ ", moddate=" + moddate ;
	}
	
	
	
	
	

}
